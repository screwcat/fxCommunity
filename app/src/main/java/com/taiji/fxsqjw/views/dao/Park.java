package com.taiji.fxsqjw.views.dao;

public class Park{

    private String guid;

    private String created_userid;

    private int is_normal;

    private String space_code;

    private String space_no;

    private int space_order;

    private String space_remark;

    private String tenants_id;

    private String update_userid;


    public String getGuid() {
        return guid;
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }

    public String getCreated_userid() {
        return created_userid;
    }

    public void setCreated_userid(String created_userid) {
        this.created_userid = created_userid;
    }

    public int getIs_normal() {
        return is_normal;
    }

    public void setIs_normal(int is_normal) {
        this.is_normal = is_normal;
    }

    public String getSpace_code() {
        return space_code;
    }

    public void setSpace_code(String space_code) {
        this.space_code = space_code;
    }

    public String getSpace_no() {
        return space_no;
    }

    public void setSpace_no(String space_no) {
        this.space_no = space_no;
    }

    public int getSpace_order() {
        return space_order;
    }

    public void setSpace_order(int space_order) {
        this.space_order = space_order;
    }

    public String getSpace_remark() {
        return space_remark;
    }

    public void setSpace_remark(String space_remark) {
        this.space_remark = space_remark;
    }

    public String getTenants_id() {
        return tenants_id;
    }

    public void setTenants_id(String tenants_id) {
        this.tenants_id = tenants_id;
    }

    public String getUpdate_userid() {
        return update_userid;
    }

    public void setUpdate_userid(String update_userid) {
        this.update_userid = update_userid;
    }
}
